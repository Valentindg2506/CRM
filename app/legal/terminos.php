<h2>2. Objeto del contrato</h2>
<p>Tinoprop pone a disposición del Cliente, mediante suscripción, acceso a la plataforma de software CRM inmobiliario <strong>Tinoprop</strong> en modalidad SaaS (Software as a Service), que incluye:</p>
<ul>
    <li>Gestión de prospectos, clientes y propiedades.</li>
    <li>Pipeline de ventas y seguimiento de actividad comercial.</li>
    <li>Integración con WhatsApp Business (Meta Cloud API) y email.</li>
    <li>Asistente de inteligencia artificial (Claude / Groq).</li>
    <li>Gestión de funnels, landing pages y formularios de captación.</li>
    <li>Módulo de facturación y pagos (integración Stripe).</li>
    <li>Sistema de afiliados y referidos.</li>
    <li>Módulos adicionales según el plan contratado.</li>
</ul>
<p>El servicio se presta en modo alojado en servidores de Tinoprop ubicados en España (UE). El Cliente no recibe el código fuente ni licencia de instalación propia.</p>

<h2>3. Alta, acceso y seguridad de la cuenta</h2>
<p>El Cliente es responsable de:</p>
<ul>
    <li>Proporcionar información veraz y actualizada durante el registro.</li>
    <li>Mantener la confidencialidad de sus credenciales de acceso.</li>
    <li>Notificar de forma inmediata a Tinoprop cualquier uso no autorizado de su cuenta.</li>
    <li>Todos los actos realizados desde su cuenta, incluyendo los de sus usuarios/agentes.</li>
</ul>
<p>El Cliente puede crear subcuentas de agentes dentro de su organización. Cada usuario creado queda bajo la responsabilidad del Cliente.</p>

<h2>4. Planes, precios y facturación</h2>
<p>El servicio se factura según el plan elegido por el Cliente (mensual o anual). Los precios vigentes se publican en <span class="placeholder">[URL DE PRECIOS]</span> y pueden modificarse con 30 días de preaviso.</p>
<ul>
    <li>El pago se procesa a través de <strong>Stripe, Inc.</strong> El Cliente acepta los Términos de Stripe al proporcionar los datos de pago.</li>
    <li>Las facturas se emiten electrónicamente y se ponen a disposición en la Plataforma.</li>
    <li>La falta de pago en el plazo establecido podrá dar lugar a la suspensión del acceso previo aviso de 7 días.</li>
    <li>Los precios no incluyen el IVA aplicable según la normativa vigente.</li>
</ul>

<h2>5. Obligaciones del Cliente</h2>
<p>El Cliente se compromete a:</p>
<ul>
    <li>Usar la Plataforma conforme a la legalidad vigente, incluyendo la normativa RGPD/LOPDGDD en su calidad de Responsable del Tratamiento de los datos de sus contactos.</li>
    <li>Disponer de la base legal adecuada antes de introducir datos personales de terceros en la Plataforma.</li>
    <li>No utilizar la Plataforma para enviar comunicaciones comerciales no solicitadas (spam).</li>
    <li>No realizar ingeniería inversa, descompilar o desensamblar el software.</li>
    <li>No subarrendar ni ceder el acceso a la Plataforma a terceros no autorizados.</li>
    <li>Respetar los límites de uso definidos en su plan.</li>
</ul>

<h2>6. Obligaciones de Tinoprop</h2>
<p>Tinoprop se compromete a:</p>
<ul>
    <li>Prestar el servicio con una disponibilidad objetivo del <strong>99% mensual</strong>, excluidos mantenimientos programados (notificados con 48 h de antelación) y causas de fuerza mayor.</li>
    <li>Mantener medidas de seguridad técnicas y organizativas adecuadas para proteger los datos del Cliente.</li>
    <li>Proporcionar soporte técnico por email en días laborables.</li>
    <li>Actuar como Encargado del Tratamiento conforme al DPA firmado con el Cliente.</li>
    <li>Notificar brechas de seguridad que afecten a datos del Cliente en un plazo máximo de 72 horas desde su detección.</li>
</ul>

<h2>7. Propiedad de los datos</h2>
<p>Los datos introducidos por el Cliente en la Plataforma son y seguirán siendo propiedad exclusiva del Cliente. Tinoprop no adquiere ningún derecho sobre dichos datos más allá de los necesarios para prestar el servicio.</p>
<p>Al finalizar el contrato, Tinoprop pondrá a disposición del Cliente una exportación completa de sus datos en formato CSV durante un plazo de <strong>30 días</strong>. Transcurrido dicho plazo, los datos serán eliminados de forma segura.</p>

<h2>8. Confidencialidad</h2>
<p>Ambas partes se comprometen a mantener la confidencialidad de la información de la otra parte que sea marcada como confidencial o que, por su naturaleza, deba entenderse como tal. Esta obligación subsiste durante 3 años tras la finalización del contrato.</p>

<h2>9. Suspensión y resolución del contrato</h2>
<p>Tinoprop podrá suspender o resolver el contrato con efecto inmediato si el Cliente:</p>
<ul>
    <li>Incumple las obligaciones establecidas en §5.</li>
    <li>Usa la Plataforma para actividades ilegales.</li>
    <li>Supera reiteradamente los límites de uso tras apercibimiento.</li>
</ul>
<p>El Cliente puede resolver el contrato en cualquier momento notificándolo con 30 días de antelación. No se realizarán reembolsos de períodos ya facturados, salvo acuerdo expreso.</p>

<h2>10. Limitación de responsabilidad</h2>
<p>La responsabilidad total de Tinoprop frente al Cliente en relación con este contrato, por cualquier causa y con independencia de la acción ejercitada, queda limitada al importe pagado por el Cliente en los <strong>12 meses anteriores</strong> al hecho causante del daño.</p>
<p>En ningún caso Tinoprop será responsable de daños indirectos, pérdidas de beneficio, de datos o de negocio, aunque hubiera sido advertida de la posibilidad de tales daños.</p>

<h2>11. Modificaciones del servicio</h2>
<p>Tinoprop podrá modificar las funcionalidades de la Plataforma con el fin de mejorarla. Los cambios que supongan una reducción sustancial de prestaciones se comunicarán con 30 días de antelación, pudiendo el Cliente resolver el contrato sin penalización en dicho plazo.</p>

<h2>12. Legislación aplicable y resolución de disputas</h2>
